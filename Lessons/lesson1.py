# What is a Variable?
# A variable is like a container that holds information. You can put different types of information in it, like numbers or words.
# Variables allow you to store and manipulate data in your programs.

# Let's create a variable to hold a name.
name = "Alice"  # Here, we are storing the word 'Alice' in a container called 'name'.

# How to Display Information?
# We can display information stored in variables using the 'print' function.
# The 'print' function shows the output on the screen.

# Let's display a message using the variable.
print("Hello, " + name + "!")  # This will print: Hello, Alice!

